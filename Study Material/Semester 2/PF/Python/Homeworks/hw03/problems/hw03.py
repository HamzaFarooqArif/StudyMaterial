def zero(f): # f is applied 0 times on x
    return lambda x: x

def successor(n):# takes three arguments n, n further takes f and f further takes x
    return lambda f: lambda x: f(n(f)(x)) # apply f on the church numeral

def one(f): # a function that applies given f on x 1 time
    """Church numeral 1: same as successor(zero)"""
    return lambda x: f(x)

def two(f): # a function that applies given f on x 2 time
    """Church numeral 2: same as successor(successor(zero))"""
    return lambda x: f(f(x))

three = successor(two)

def church_to_int(n): # n is any church numeral, not a numeral
    """Convert the Church numeral n to a Python integer.

    >>> church_to_int(zero)
    0
    >>> church_to_int(one)
    1
    >>> church_to_int(two)
    2
    >>> church_to_int(three)
    3
    """
    return n(lambda x: x + 1)(0) # the more we apply x + 1 with scnd pos arg 0 larger we get the number

def add_church(m, n):
    """Return the Church numeral for m + n, for Church numerals m and n.

    >>> church_to_int(add_church(two, three))
    5
    """
    return lambda f: lambda x: m(f)(n(f)(x)) # m(f)(x) x may be base 0 or some lambdas 'x+1' applied to 0.Ex:add_church(m, n) apply m functions on (apply n functions on 0)

def mul_church(m, n):
    """Return the Church numeral for m * n, for Church numerals m and n.

    >>> four = successor(three)
    >>> church_to_int(mul_church(two, three))
    6
    >>> church_to_int(mul_church(three, four))
    12
    """
    return lambda f: lambda x: m(n(f))(x)

def pow_church(m, n):
    """Return the Church numeral m ** n, for Church numerals m and n.

    >>> church_to_int(pow_church(two, three))
    8
    >>> church_to_int(pow_church(three, two))
    9
    """
    return lambda f: lambda x: (n)(m)(f)(x)
